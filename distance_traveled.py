# This program calculates the distanced traveled
# of a set variable for speed and
# variables for time.

# Set the variable for speed

speed = 70

# Set variables for time

time_1 = 6
time_2 = 10
time_3 = 15

# Calculate the distances traveled

distance_1 = speed * time_1
distance_2 = speed * time_2
distance_3 = speed * time_3


# Display the distances

print("You have traveled", \
      format(distance_1, ',.2f'))
print("You have traveled", \
      format(distance_2, ',.2f'))
print("You have traveled", \
      format(distance_3, ',.2f'))
